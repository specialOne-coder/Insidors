function countAndSortTimeIntervals(priceHistory) {
    const intervalCounts = {};
    console.log(priceHistory[0].unixTime);

    // Capture le premier et le dernier timestamp
    const firstTimestamp = priceHistory[0].unixTime;
    const lastTimestamp = priceHistory[priceHistory.length - 1].unixTime;

    for (let i = 1; i < priceHistory.length; i++) {
        const previousTimestamp = priceHistory[i - 1].unixTime;
        const currentTimestamp = priceHistory[i].unixTime;

        // Calcul de l'intervalle en millisecondes
        const interval = currentTimestamp - previousTimestamp;

        // Conversion en minutes
        const intervalInMinutes = interval / (1 * 60);

        // Si cet intervalle existe déjà dans notre objet, on incrémente le compteur
        if (intervalCounts[intervalInMinutes]) {
            intervalCounts[intervalInMinutes]++;
        } else {
            // Sinon, on initialise ce compteur à 1
            intervalCounts[intervalInMinutes] = 1;
        }
    }

    // Convertir l'objet en tableau de tuples, trier par valeur (le nombre d'occurrences) du plus grand au plus petit
    const sortedArray = Object.entries(intervalCounts).sort((a, b) => b[1] - a[1]);

    // Retourner les résultats avec les timestamps
    console.log({firstTimestamp});
    
    return {
        firstTimestamp: new Date(firstTimestamp*1000).toLocaleString(),
        lastTimestamp: new Date(lastTimestamp*1000).toLocaleString(),
        sortedIntervals: sortedArray
    };
}


